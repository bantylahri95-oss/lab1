# Program to calculate the perimeter of a triangle

# Taking input from user
side_one = float(input("Enter the length of first side: "))
side_two = float(input("Enter the length of second side: "))
side_three = float(input("Enter the length of third side: "))

# Calculating perimeter
triangle_perimeter = side_one + side_two + side_three

# Displaying result
print("\n----- Result -----")
print("Perimeter of the triangle is:", triangle_perimeter, "cm")